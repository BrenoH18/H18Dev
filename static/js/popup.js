 // Função para abrir o pop-up
 function openPopupAbrirCaixa() {
    document.getElementById("popup-abrir-caixa").style.display = "block";
    document.getElementById("overlay").style.display = "block";
}

// Função para fechar o pop-up
function closePopupAbrirCaixa() {
    document.getElementById("popup-abrir-caixa").style.display = "none";
    document.getElementById("overlay").style.display = "none";
}

